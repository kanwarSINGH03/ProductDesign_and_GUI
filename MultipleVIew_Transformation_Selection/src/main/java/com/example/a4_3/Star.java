package com.example.a4_3;

public class Star {
    private double x;
    private double y;
    public Star(double x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }

    public void setX(double x)
    {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
